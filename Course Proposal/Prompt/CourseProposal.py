
def get_course_proposal_prompt(course_details):
    '''
    Generate a course proposal prompt based on the provided course details. 
    '''

    prompt = f"""
        قم بتطوير مقترح دورة تدريبية حول الموضوع التالي باستخدام المعلومات المقدمة فقط. يجب أن يكون المقترح جاهزًا للتقديم مباشرةً إلى المستخدم دون ترك أي حقول فارغة أو استخدام نصوص مثل "[أدخل...]".

        **عنوان الدورة:**
        {course_details['subject']}

        **نظرة عامة على الدورة:**
        اكتب نظرة عامة عن الدورة توضح الهدف منها وما ستقدمه للمشاركين. الدورة تهدف إلى تقديم معرفة شاملة حول {course_details['subject']}، مع التركيز على {course_details.get('key_points', 'المفاهيم الأساسية والتطبيقات العملية')}. الدورة مصممة لتلبية احتياجات {course_details.get('target_audience', 'المبتدئين')}.

        **أهداف التعلم:**
        اكتب أهداف التعلم التي ستتحقق بعد إتمام الدورة. يجب أن تشمل:
        - فهم المفاهيم الأساسية المتعلقة بـ {course_details['subject']}.
        - تطوير المهارات اللازمة للتعامل مع {course_details.get('key_points', 'المفاهيم الأساسية والتطبيقات العملية')}.
        - تعزيز المعرفة العملية والنظرية في مجال {course_details['subject']}.

        **الفئة المستهدفة:**
        اكتب وصفًا للفئة المستهدفة من الدورة مع ذكر المهارات أو الخلفية المطلوبة. الفئة المستهدفة هي {course_details.get('target_audience', 'المبتدئين')}. يجب أن تتضمن الوصف المهارات أو الخلفية التي يحتاجها المشاركون للاستفادة من الدورة.

        **مدة الدورة:**
        اكتب تفاصيل عن مدة الدورة. عدد ساعات التدريب هو {course_details.get('duration', '30')} ساعه . 

        **مخطط الدورة:**
        اكتب مخططًا تفصيليًا للدورة يشمل الوحدات والمواضيع التي سيتم تغطيتها. يجب أن يكون المخطط مرنًا ويتضمن عددًا مناسبًا من الوحدات بناءً على محتوى الدورة. قم بتوزيع ساعات الدورة ({course_details.get('duration', '30')} ساعه)  على الوحدات بشكل متوازن.

        **طرق التدريس:**
        اكتب وصفًا لطرق التدريس التي سيتم استخدامها في الدورة. طريقة التدريب هي {course_details['training_method']}.

        **طرق التقييم:**
        اكتب وصفًا لطرق التقييم التي سيتم استخدامها في الدورة. التقييم يشمل:
        - تقييم المعرفة من خلال الاختبارات القصيرة والمشاريع الجماعية.
        """

    if 'other_information' in course_details:
        prompt += f"""

        **معلومات إضافية:**
        اكتب أي معلومات إضافية عن الدورة. يجب أن يتم دمج المعلومات الإضافية ({course_details['other_information']}) في المقترح بشكل طبيعي دون ذكرها كقسم منفصل.
        """

    prompt += """

    **ملاحظة:** يجب أن يكون المقترح جاهزًا للتقديم مباشرةً إلى المستخدم. لا تقم بإضافة معلومات إضافية أو طلب معلومات أخرى.
    """
    return prompt
